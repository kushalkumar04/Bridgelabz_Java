package coreprogramming.programmingelements.level1;

/*
 * This class calculates and prints the average of marks
 * obtained in three subjects: Mathematics, Physics, and Chemistry.
 */
public class AverageMark {

    public static void main(String[] args) {

        // Store marks of three subjects
        int maths = 94;
        int phy = 95;
        int chem = 96;

        // Calculate and print the average of the three marks
        System.out.println((maths + phy + chem) / 3);
    }
}